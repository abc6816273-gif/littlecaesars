#!/usr/bin/env node

/**
 * Force update coupons script
 * Run with: node scripts/force-update-coupons.js
 */

require('dotenv').config({ path: '.env.local' });

async function forceUpdateCoupons() {
  console.log('ğŸš€ Force updating coupons...\n');
  
  const baseUrl = process.env.NEXT_PUBLIC_SITE_URL || 'http://localhost:3000';
  
  try {
    console.log('ğŸ“¤ Calling POST /api/update-coupons...');
    
    const response = await fetch(`${baseUrl}/api/update-coupons`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'User-Agent': 'force-update-script'
      }
    });
    
    console.log(`ğŸ“¥ Response status: ${response.status} ${response.statusText}`);
    
    if (response.ok) {
      const data = await response.json();
      console.log('\nâœ… Success!');
      console.log(`ğŸ“Š Generated ${data.data?.coupons?.length || 0} coupons`);
      console.log(`ğŸ• Last updated: ${data.data?.lastUpdated || 'Unknown'}`);
      console.log(`ğŸ¤– Generated by: ${data.generatedBy || 'Unknown'}`);
      console.log(`ğŸ”— Google Indexing: ${data.googleIndexing ? 'Triggered' : 'Skipped'}`);
      
      // Show first few coupons
      if (data.data?.coupons?.length > 0) {
        console.log('\nğŸ« Sample coupons:');
        data.data.coupons.slice(0, 3).forEach((coupon, index) => {
          console.log(`   ${index + 1}. ${coupon.code} - ${coupon.discount}`);
          console.log(`      ${coupon.description}`);
        });
      }
    } else {
      const errorData = await response.json();
      console.log('\nâŒ Error:');
      console.log(`   Message: ${errorData.message || 'Unknown error'}`);
      console.log(`   Error: ${errorData.error || 'No details'}`);
    }
    
  } catch (error) {
    console.error('\nâŒ Failed to update coupons:', error.message);
    
    if (error.message.includes('ECONNREFUSED')) {
      console.log('\nğŸ’¡ Tips:');
      console.log('   1. Make sure your development server is running: npm run dev');
      console.log('   2. Or update NEXT_PUBLIC_SITE_URL to your live site URL');
    }
  }
}

